import java.util.*;
public class SubString {
    public static void main(String[] args) {
        Scanner sc= new Scanner(System.in);
        System.out.print("ENTRE THE NAME : ");
        String name= sc.nextLine();
        System.out.println(name.substring(5,name.length()));
    }
}

